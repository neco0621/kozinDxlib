#include "Rect.h"
#include <cassert>

Rect::Rect() :
	m_left(0.0f),
	m_top(0.0f),
	m_right(0.0f),
	m_bottom(0.0f)
{

}

Rect::~Rect()
{
}

void Rect::Set(float left, float top, float width, float height)
{
	m_left = left;			//左上のX座標
	m_top = top;			//左上のY座標
	m_right = left + width;	//右下のX座標
	m_bottom = top + height;//右下のY座標
}

float Rect::GetWidth() const
{
	assert(m_right >= m_left);	//左右の座標の入れ替わりチェック

	return m_right - m_left;
}

float Rect::GetHeight() const
{
	assert(m_bottom >= m_top);	//上下の座標の入れ替わりチェック

	return m_bottom - m_top;
}

Vec2 Rect::GetCenter() const
{
	//中心座標
	float x = 0.0f;
	float y = 0.0f;

	x = (m_right + m_left) / 2;
	y = (m_top + m_bottom) / 2;

	return Vec2{ x,y };
}

bool Rect::IsCollision(const Rect& rect)
{
	//絶対に当たらないパターンをはじいていく
	if (m_left > rect.m_right)	return false;
	if (m_top > rect.m_bottom)	return false;
	if (m_right < rect.m_left)	return false;
	if (m_bottom < rect.m_top)	return false;

	//当たらないパターン以外は当たっている
	return true;
}
